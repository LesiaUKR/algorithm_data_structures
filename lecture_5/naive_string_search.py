# –ù–∞—ó–≤–Ω–∏–π –∞–ª–≥–æ—Ä–∏—Ç–º –ø–æ—à—É–∫—É –≤ —Ä—è–¥–∫–∞—Ö - —Å–ø–æ—Å–æ–±—ñ–≤ –∑–Ω–∞—Ö–æ–¥–∂–µ–Ω–Ω—è –ø—ñ–¥—Ä—è–¥–∫–∞ –≤ –≥–æ–ª–æ–≤–Ω–æ–º—É —Ä—è–¥–∫—É.
# –í—ñ–Ω –ø–æ–ª—è–≥–∞—î –≤ –ø–æ—Å–ª—ñ–¥–æ–≤–Ω–æ–º—É –ø–æ—Ä—ñ–≤–Ω—è–Ω–Ω—ñ –ø—ñ–¥—Ä—è–¥–∫–∞ –∑ –∫–æ–∂–Ω–∏–º –≤—ñ–¥—Ä—ñ–∑–∫–æ–º –≥–æ–ª–æ–≤–Ω–æ–≥–æ —Ä—è–¥–∫–∞
# –û—Å–Ω–æ–≤–Ω–∏–π –ø—Ä–∏–Ω—Ü–∏–ø –∞–ª–≥–æ—Ä–∏—Ç–º—É ‚Äî —Ü–µ –ø–æ—Ä—ñ–≤–Ω—è–Ω–Ω—è –∫–æ–∂–Ω–æ–≥–æ —Å–∏–º–≤–æ–ª—É –≥–æ–ª–æ–≤–Ω–æ–≥–æ —Ä—è–¥–∫–∞ –∑ –ø–µ—Ä—à–∏–º —Å–∏–º–≤–æ–ª–æ–º —à–∞–±–ª–æ–Ω—É. 
# –Ø–∫—â–æ –≤–æ–Ω–∏ –∑–±—ñ–≥–∞—é—Ç—å—Å—è, –Ω–µ–æ–±—Ö—ñ–¥–Ω–æ –ø–æ—Ä—ñ–≤–Ω—è—Ç–∏ –Ω–∞—Å—Ç—É–ø–Ω—ñ —Å–∏–º–≤–æ–ª–∏ –¥–æ –∫—ñ–Ω—Ü—è —à–∞–±–ª–æ–Ω—É.
# —Å–∫–ª–∞–¥–Ω—ñ—Å—Ç—å O(n√óm), –¥–µ n ‚Äî –¥–æ–≤–∂–∏–Ω–∞ –≥–æ–ª–æ–≤–Ω–æ–≥–æ —Ä—è–¥–∫–∞, –∞ m ‚Äî –¥–æ–≤–∂–∏–Ω–∞ –ø—ñ–¥—Ä—è–¥–∫–∞
# –ø—Ä–æ—Ç–µ, —è–∫—â–æ –¥–æ–≤–∂–∏–Ω–∞ –ø—ñ–¥—Ä—è–¥–∫–∞ –Ω–∞–±–ª–∏–∂–∞—î—Ç—å—Å—è –¥–æ –¥–æ–≤–∂–∏–Ω–∏ –≥–æ–ª–æ–≤–Ω–æ–≥–æ —Ä—è–¥–∫–∞, –∞–ª–≥–æ—Ä–∏—Ç–º —Å—Ç–∞—î –Ω–µ–µ—Ñ–µ–∫—Ç–∏–≤–Ω–∏–º,
# –±–æ —á–∞—Å–æ–≤–∞ —Å–∫–ª–∞–¥–Ω—ñ—Å—Ç—å —Å—Ç–∞—î –∫–≤–∞–¥—Ä–∞—Ç–∏—á–Ω–æ—é ùëÇ(ùëõ2)

# —î –±—ñ–ª—å—à –µ—Ñ–µ–∫—Ç–∏–≤–Ω—ñ –∞–ª–≥–æ—Ä–∏—Ç–º–∏ –ø–æ—à—É–∫—É –ø—ñ–¥—Ä—è–¥–∫–∞ –≤ —Ä—è–¥–∫—É:
# –ê–ª–≥–æ—Ä–∏—Ç–º –ö–Ω—É—Ç–∞-–ú–æ—Ä—Ä—ñ—Å–∞-–ü—Ä–∞—Ç—Ç–∞ (–ö–ú–ü)
# –ê–ª–≥–æ—Ä–∏—Ç–º –ë–æ—î—Ä–∞-–ú—É—Ä–∞
# –ê–ª–≥–æ—Ä–∏—Ç–º –†–∞–±—ñ–Ω–∞-–ö–∞—Ä–ø–∞

def naive_search(main_string, pattern):
    M = len(pattern)
    N = len(main_string)

    # –ü–µ—Ä–µ–±—ñ—Ä –ø–æ —Å–∏–º–≤–æ–ª–∞—Ö –≥–æ–ª–æ–≤–Ω–æ–≥–æ —Ä—è–¥–∫–∞
    for i in range(N - M + 1):
        j = 0

        # –ü–µ—Ä–µ–±—ñ—Ä –ø–æ —Å–∏–º–≤–æ–ª–∞—Ö –ø—ñ–¥—Ä—è–¥–∫–∞
        while j < M:
            if main_string[i + j] != pattern[j]:
                break
            j += 1

        # –Ø–∫—â–æ –∑–Ω–∞—á–µ–Ω–Ω—è j –¥–æ—Ä—ñ–≤–Ω—é—î –¥–æ–≤–∂–∏–Ω—ñ –ø—ñ–¥—Ä—è–¥–∫–∞, —Ç–æ –ø—ñ–¥—Ä—è–¥–æ–∫ –∑–Ω–∞–π–¥–µ–Ω–æ
        if j == M:
            return i

    return -1

main_string = "ABABDABACDABABCABAB"
pattern = "ABABCABAB"
position = naive_search(main_string, pattern)

if position != -1:
    print(f"–ü—ñ–¥—Ä—è–¥–æ–∫ –∑–Ω–∞–π–¥–µ–Ω–æ –Ω–∞ –ø–æ–∑–∏—Ü—ñ—ó {position}")
else:
    print("–ü—ñ–¥—Ä—è–¥–æ–∫ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ –≤ –≥–æ–ª–æ–≤–Ω–æ–º—É —Ä—è–¥–∫—É.")
